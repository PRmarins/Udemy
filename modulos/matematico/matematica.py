import math

# Acesar o número pi
print(f"{math.pi:.2f}")

# Acesar o número de Euler
print(f"{math.e:.3f}")

# Arredondar um número para cima e para baixo
print(f"{math.ceil(100/7)}")

print(f"{math.floor(100/7)}")

# Fatorial de um número
print(f"{math.factorial(5)}")


